                #PYTHON LITETALS:
#Def: Literals- A Literal is just a fixed value 
      # or constant written directly in the code.

#String Literals
NAME = 'PYTHON'
print("String Literals:",NAME)

#Numeric Literals
AGE = 20
HEIGHT = 5.9
COMPLEX_NUM = 20+30j
print("Integer Literal: ",AGE)
print("Float Literal: ",HEIGHT)
print("Complex Literal: ",COMPLEX_NUM)

#Boolean Literal
IS_STUDENT = True
print("Boolean Literal: ",IS_STUDENT)

#Special Literal
NONE_VALUE = None
print("Special Literal(None): ",NONE_VALUE)

#Literal Collections
FRUITS = ("apple","Banana","Cherry") #Tuple
#Tuple - Group of Items Ordered, Unchangeable
COLOURS = ["red","Green","blue"] #List
#List- Group of Items Ordered,Changeable
PERSON = {"Name" : "PYTHON","Age" : 26} #Dictionary
#Dictionary - Key Value Pairs
VOWELS = {'a','e','i','o','u'} #set
#set - Group of Unique items, No duplicates.

print("Tuple Literal:",FRUITS)
print("List Literal:",COLOURS)
print("Dictionary Literal:",PERSON)
print("set Literal:",VOWELS)


